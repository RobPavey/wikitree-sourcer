function getOtherParishDataFromNameAndCongregation(parishAndCongregationName) {
  if (!parishAndCongregationName) {
    return [];
  }

  let ucName = parishAndCongregationName.trim().toUpperCase();

  if (!ucName) {
    return [];
  }

  const result = other_churches.filter((parish) => {
    if (ucName == parish.other_church) {
      return true;
    }
    return false;
  });

  return result;
}

