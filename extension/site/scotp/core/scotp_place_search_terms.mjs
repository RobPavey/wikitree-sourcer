function findKeyMatch(searchTermObject, ucName, resultArray) {
  let lookupKey = ucName.replace(/\s/g, "%20").replace(/\'/g, "%27");

  let keyMatch = searchTermObject[lookupKey];
  if (keyMatch) {
    resultArray.push(keyMatch);
  }
}

function findSearchTermMatches(searchTermObject, ucName, resultArray) {
  const searchTerms = Object.values(searchTermObject);
  for (let searchTerm of searchTerms) {
    // remove the underscore and following part - it is the same as the key which we
    // will have already tested for
    let termString = searchTerm;
    let underscoreIndex = searchTerm.indexOf("_");
    if (underscoreIndex != -1) {
      termString = termString.substring(0, underscoreIndex);
    }

    // split into the separate place names
    let placeArray = termString.split("|");

    if (placeArray.includes(ucName)) {
      resultArray.push(searchTerm);
    }
  }
}

function findSearchTermFuzzyMatches(searchTermObject, ucName, resultArray) {
  console.log("findSearchTermFuzzyMatches: ucName is: " + ucName);

  const searchTerms = Object.values(searchTermObject);
  for (let searchTerm of searchTerms) {
    // remove the underscore and following part - it is the same as the key which we
    // will have already tested for
    let termString = searchTerm;
    let underscoreIndex = searchTerm.indexOf("_");
    if (underscoreIndex != -1) {
      termString = termString.substring(0, underscoreIndex);
    }

    // split into the separate place names
    let placeArray = termString.split("|");

    // if the given name is includes as a word
    for (let place of placeArray) {
      // All the ucName words should be in the place name, in the same order
      // They cannot be part of a word. So "Monzie" should not match "Monzievaird"
      //  This is TBD
      if (place.includes(ucName)) {
        resultArray.push(searchTerm);
      }
    }
  }
}

function findMatches(searchTermObject, ucName, includeFuzzyMatches, onlyOneExact) {
  let matches = [];
  findKeyMatch(searchTermObject, ucName, matches);

  if (matches.length === 0) {
    findSearchTermMatches(searchTermObject, ucName, matches);
  }

  let includedFuzzyMatches = false;
  if (matches.length === 0 && includeFuzzyMatches) {
    findSearchTermFuzzyMatches(searchTermObject, ucName, matches);
    includedFuzzyMatches = true;
  }

  if (matches.length == 0) {
    console.log("findMatches: ucName is: '" + ucName + "' and there are no matches");
    return [];
  }

  if (!includedFuzzyMatches && onlyOneExact && matches.length > 1) {
    // if we get multiple non-fuzzy hits that we think that any one of them is sufficient on its own.
    // So pick the shortest one.
    let shortestMatch = matches[0];
    for (let i = 1; i < matches.length; i += 1) {
      let thisMatch = matches[i];
      if (thisMatch.length < shortestMatch.length) {
        shortestMatch = thisMatch;
      }
    }
    matches = [shortestMatch];
  }

  return matches;
}

// Looks up the list of Congregations, Parishes or Districts and returns
// the search criteria required to create the URI

function getStatutorySearchParameters(name, includeFuzzyMatches) {
  const result = [];
  let ucName = name.toUpperCase();

  if (includeFuzzyMatches) {
    ucName = ucName.replace(/^ST\.\s+/, "ST ");
    ucName = ucName.replace(/^ST\./, "ST ");
    ucName = ucName.replace(/^SAINT\s+/, "ST ");
  }

  let matches = findMatches(statutoryDistricts, ucName, includeFuzzyMatches, true);

  // Special case: The name "Service Returns" needs to have both entries from statutoryDistricts
  // added (the one with a single space and the one with a double space). This seems like a
  // bug in Scotlands People site.
  if (ucName == "SERVICE RETURNS" || ucName == "SERVICE  RETURNS") {
    if (matches.length == 1) {
      let match = matches[0];
      if (match == "SERVICE RETURNS_SERVICE RETURNS") {
        matches.push("SERVICE  RETURNS_SERVICE  RETURNS");
      } else if (match == "SERVICE  RETURNS_SERVICE  RETURNS") {
        matches.push("SERVICE RETURNS_SERVICE RETURNS");
      }
    }
  }

  for (let match of matches) {
    //let idx = match.lastIndexOf("_");

    let real_name = ucName; // it turns out this has to be present but does nothing
    // let real_name = match.substring(0,idx).replace(/\|/g," OR ");
    let display_name = match;
    let rdno = " "; // it turns out this has to be present but does nothing
    //let rdno = match.substring(0,idx).replace(/\|/g," OR ");

    result.push({
      real_name: real_name,
      display_name: display_name,
      rdno: rdno,
    });
  }
  return result;
}

function getOprSearchParameters(name, includeFuzzyMatches) {
  // Given the value from the "Parish" column of the OPR search results this will return the
  // search parameters to use in a generated citation URL.

  const result = [];
  let ucName = name.toUpperCase();

  let matches = findMatches(oprParishes, ucName, includeFuzzyMatches, true);

  for (let i = 0; i < matches.length; i += 1) {
    let underscoreIndex = matches[i].lastIndexOf("_");

    let real_name = matches[i].substring(0, underscoreIndex).replace(/\|/g, " OR ");
    let display_name = matches[i];
    let label = matches[i].substring(underscoreIndex + 1);

    result.push({
      real_name: real_name,
      display_name: display_name,
      label: label,
    });
  }
  //console.log("getOprSearchParameters: name is: '" + name + "'");
  //console.log("getOprSearchParameters: result is:");
  //console.log(result);
  return result;
}

function getCatholicSearchParameters(string, includeFuzzyMatches) {
  const result = [];
  const ucName = string.toUpperCase();

  let matches = [];

  findKeyMatch(rcParishes, ucName, matches);

  if (matches.length === 0 && includeFuzzyMatches) {
    const arrSearchTerms = Object.values(rcParishes); // convert object to array and filter by values
    matches = arrSearchTerms.filter(function (value) {
      return value.includes(ucName);
    });
  }

  for (let match of matches) {
    let parishParts = match.split("|");
    if (parishParts.length === 3) {
      result.push({
        mp_code: parishParts[0],
        mp_no: parishParts[1],
        parish_title: parishParts[2],
      });
    }
  }
  return result;
}

function getPlaceSearchTerms(string, type, stringIsFromResults) {
  if (!string) {
    return undefined;
  }
  if (type === "statutory") {
    // for statutory BMDs and census
    if (stringIsFromResults) {
      return getStatutorySearchParameters(string, false);
    } else {
      return getStatutorySearchParameters(string, true);
    }
  } else if (type === "opr") {
    // for opr BMDs
    if (stringIsFromResults) {
      return getOprSearchParameters(string, false);
    } else {
      return getOprSearchParameters(string, true);
    }
  } else if (type === "rc") {
    // for catholic BMDs
    if (stringIsFromResults) {
      return getCatholicSearchParameters(string, false);
    } else {
      return getCatholicSearchParameters(string, true);
    }
  } else if (type === "other") {
    // for other presbyterian congregation BMDs
    return [{ congregation: string }]; // no lookup needed for congregations
  }
}

